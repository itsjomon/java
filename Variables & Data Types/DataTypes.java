public class DataTypes {
    public static void main(String args[]) {
        byte b = 8;
        System.out.println("Byte value: " + b); // Output will be "Byte value: 8"

        char ch = 'a';
        System.out.println("Char value: " + ch);

        boolean var = false;
        System.out.println("Boolean value: " + var);

        float price = 10.5f;
        System.out.println("Float value: " + price);

        int num = 25;
        System.out.println("Int value: " + num);

        long largeNum = 123456789L;
        System.out.println("Long value: " + largeNum); // Included to show long usage

        double preciseNum = 99.99;
        System.out.println("Double value: " + preciseNum); // Included to show double usage

        short n = 240;
        System.out.println("Short value: " + n);
    }
}



// data types (primitive and non-primitive data types)

