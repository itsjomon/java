// short : 2 bytes [-32768 to 32767]
// char : 2 bytes [a to z, A to Z, @, %,...]
// boolean : 1 bytes [true, false]
// int : 4 bytes [-2 billion to 2 billion without a decimal point]
// long : 8 bytes [more larger than int]
// float : 4 bytes [with decimal point '.']
// double : 8 bytes [More larger number with decimail point '.']


