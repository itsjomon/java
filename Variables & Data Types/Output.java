public class Output {
    public static void main(String args[]) {
        System.out.print("Hello World!"); // print in same line

        System.out.println("Hello World!");
        System.out.println("Hello World!"); // print in next line
        
        System.out.print("Hello World!\n");
        System.out.print("Hello World!\n"); 
        System.out.print("Hello World!\nHello Earth!");  // print in next line
    }
}


